"""Scan the CURRENT backend chunks_meta.json for candidate evaluation
questions from documents not yet covered by dataset.json.

This does NOT use an LLM - it is a lightweight, pattern-based scanner that
surfaces "fact-shaped" sentences (dates, definitions, named roles) from
chunks and drafts a question + a ground-truth candidate around them. It
exists to speed up manual dataset expansion, not to replace it.

*** OUTPUT IS UNVERIFIED. *** Every candidate has "needs_verification": true
and must be read by a human against the source chunk before its
ground_truth is trusted for any thesis claim (BAB IV). This mirrors the
same discipline used when dataset.json's 10 original questions were filled
in by hand - facts must trace back to an actual ingested document, not be
guessed or generated by an LLM without a citation.

Usage:
    python generate_qa_candidates.py --chunks-meta ../../backend/app/data/chunks_meta.json
    python generate_qa_candidates.py --category "Dataset_PMB_UPI" --limit 20
    python generate_qa_candidates.py --title-contains "Kalender Akademik" --limit 10
    python generate_qa_candidates.py --exclude-dataset dataset.json  # skip docs already covered

After reviewing/editing candidates.json by hand, merge the questions that
pass verification into dataset.json manually (see README.md).
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Any

# Chunks that are forms, signature blocks, or number tables produce
# unusable questions. Same discipline as the legacy build_qa_dataset.py
# generator this replaces - skip anything that looks like a template.
_BAD_STRONG = [
    "yang bertanda tangan", "materai", "pas foto", "coret yang tidak",
    "surat pernyataan", "menyanggupi pembiayaan",
]
_BAD_WEAK = [
    "..........", "nama lengkap :", "nim :", "tanda tangan", "nomor telepon/hp",
    "nama :", "alamat :",
]

# Pattern -> (question_template, label). {subject} is the text captured
# before the pattern; {value} is the text captured after it.
_FACT_PATTERNS = [
    (re.compile(r"(?P<subject>.{10,80}?) dijadwalkan pada .{0,20}?tanggal (?P<value>[^.]{5,80})", re.IGNORECASE),
     "Kapan {subject_q}?", "schedule"),
    (re.compile(r"(?P<subject>.{10,80}?) adalah (?P<value>[^.]{5,120})\.", re.IGNORECASE),
     "Apa/siapa {subject_q}?", "definition"),
    (re.compile(r"(?P<subject>.{10,80}?) dilaksanakan (?:secara |di |pada )?(?P<value>[^.]{5,100})", re.IGNORECASE),
     "Bagaimana/di mana {subject_q}?", "procedure"),
]


def good_chunk(text: str) -> bool:
    t = (text or "").strip()
    if len(t) < 220 or len(t) > 2600:
        return False
    letters = sum(c.isalpha() for c in t)
    if letters / max(len(t), 1) < 0.55:
        return False
    low = t.lower()
    if any(m in low for m in _BAD_STRONG):
        return False
    if sum(m in low for m in _BAD_WEAK) >= 2:
        return False
    if len(low.split()) < 40:
        return False
    return True


def extract_candidates(chunk: dict[str, Any]) -> list[dict]:
    text = chunk.get("text", "")
    if not good_chunk(text):
        return []

    candidates = []
    for pattern, template, label in _FACT_PATTERNS:
        for m in pattern.finditer(text):
            subject = m.group("subject").strip(" ,.-\n")
            value = m.group("value").strip(" ,.-\n")
            if len(subject) < 5 or len(value) < 3:
                continue
            # Lowercase the subject when splicing into a question, but keep
            # proper-noun-looking subjects (has uppercase mid-string) as-is.
            subject_q = subject if any(c.isupper() for c in subject[1:]) else subject[0].lower() + subject[1:]
            question = template.format(subject_q=subject_q)
            candidates.append({
                "match_type": label,
                "matched_span": m.group(0)[:300],
                "suggested_question": question,
                "suggested_ground_truth_fragment": m.group(0).strip(),
                "chunk_id": chunk["chunk_id"],
                "doc_id": chunk["doc_id"],
                "title": chunk.get("title", ""),
                "category": chunk.get("category", ""),
                "source_text_full": text,
                "needs_verification": True,
                "verified": False,
            })
            if len(candidates) >= 2:  # cap per chunk - avoid one chunk flooding the output
                break
    return candidates


def load_covered_chunk_ids(dataset_path: str | None) -> set[str]:
    if not dataset_path or not Path(dataset_path).exists():
        return set()
    dataset = json.load(open(dataset_path, encoding="utf-8"))
    covered = set()
    for item in dataset:
        covered.update(item.get("expected_chunk_ids") or [])
    return covered


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate DRAFT evaluation question candidates from chunks_meta.json (needs manual verification)")
    parser.add_argument("--chunks-meta", default="../../backend/app/data/chunks_meta.json",
                         help="Path to the backend's chunks_meta.json")
    parser.add_argument("--category", default=None, help="Only scan chunks with this exact category")
    parser.add_argument("--title-contains", default=None, help="Only scan chunks whose title contains this substring (case-insensitive)")
    parser.add_argument("--exclude-dataset", default="dataset.json",
                         help="Skip chunks already used as ground truth in this dataset.json (avoids duplicate coverage)")
    parser.add_argument("--limit", type=int, default=50, help="Max candidates to emit")
    parser.add_argument("--output", default="candidates.json", help="Output path")
    args = parser.parse_args()

    chunks = json.load(open(args.chunks_meta, encoding="utf-8"))
    covered = load_covered_chunk_ids(args.exclude_dataset)

    if args.category:
        chunks = [c for c in chunks if c.get("category") == args.category]
    if args.title_contains:
        needle = args.title_contains.lower()
        chunks = [c for c in chunks if needle in (c.get("title") or "").lower()]
    if covered:
        chunks = [c for c in chunks if c["chunk_id"] not in covered]

    print(f"Scanning {len(chunks)} chunks (after filters, {len(covered)} chunk_ids excluded as already covered)...")

    all_candidates: list[dict] = []
    for chunk in chunks:
        if len(all_candidates) >= args.limit:
            break
        all_candidates.extend(extract_candidates(chunk))

    all_candidates = all_candidates[:args.limit]

    output_path = Path(args.output)
    output_path.write_text(json.dumps(all_candidates, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"Generated {len(all_candidates)} DRAFT candidates -> {output_path}")
    print("*** UNVERIFIED *** - read each candidate against source_text_full before trusting")
    print("its suggested_ground_truth_fragment. Delete/fix any that are wrong, then manually")
    print("merge the survivors into dataset.json (see README.md for the schema).")
    return 0


if __name__ == "__main__":
    sys.exit(main())
